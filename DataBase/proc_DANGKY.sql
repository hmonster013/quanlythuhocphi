CREATE PROCEDURE sp_DANGKY_insert(
	@MADK int,
	@MALHP int,
	@MASV nvarchar(10),
	@HUYDANGKY bit
)
AS
BEGIN
	INSERT INTO DANGKY VALUES (@MADK, @MALHP, @MASV, @HUYDANGKY)
END
GO

CREATE PROCEDURE sp_DANGKY_update(
	@MADK int,
	@MALHP int,
	@MASV nvarchar(10),
	@HUYDANGKY bit
)
AS
BEGIN
	UPDATE DANGKY SET MALHP = @MALHP, MASV = @MASV, HUYDANGKY = @HUYDANGKY WHERE MADK = @MADK
END
GO

CREATE PROCEDURE sp_DANGKY_delete(
	@MADK int
)
AS
BEGIN
	DELETE FROM DANGKY WHERE MADK = @MADK
END
GO

CREATE PROCEDURE sp_DANGKY_select_all
AS
BEGIN
	SELECT * FROM DANGKY
END
GO

CREATE PROCEDURE sp_DANGKY_select_madk(
	@MADK int
)
AS
BEGIN
	SELECT * FROM DANGKY WHERE MADK = @MADK
END
GO
