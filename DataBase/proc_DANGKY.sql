USE QLSV_TC
GO

CREATE PROCEDURE sp_DANGKY_insert(
	@MASV nvarchar(15),
	@HOCKY int
)
AS
BEGIN
	INSERT INTO DANGKY(MASV, HOCKY) VALUES (@MASV, @HOCKY)
END
GO

CREATE PROCEDURE sp_DANGKY_update(
	@MADK int,
	@MASV nvarchar(15),
	@HOCKY int
)
AS
BEGIN
	UPDATE DANGKY SET MASV = @MASV, HOCKY = @HOCKY WHERE MADK = @MADK
END
GO

CREATE PROCEDURE sp_DANGKY_delete(
	@MADK int
)
AS
BEGIN
	DELETE FROM DANGKY WHERE MADK = @MADK
END
GO

CREATE PROCEDURE sp_DANGKY_select_all
AS
BEGIN
	SELECT * FROM DANGKY
END
GO

CREATE PROCEDURE sp_DANGKY_select_madk(
	@MADK int
)
AS
BEGIN
	SELECT * FROM DANGKY WHERE MADK = @MADK
END


CREATE PROCEDURE sp_DANGKY_select_masv(
	@MASV nvarchar(15)
)
as
begin
	select * from DANGKY where MASV = @MASV
end
go

CREATE PROCEDURE sp_DANGKY_select_masv_hocky(
	@MASV nvarchar(15),
	@HOCKY int
)
as
begin
	select * from DANGKY where MASV = @MASV and HOCKY = @HOCKY
end
go

