USE QLSV_TC
GO

CREATE PROCEDURE sp_PHIEUTHU_insert(
@MASV nvarchar(15),
@NIENKHOA nvarchar(9),
@HOCKY int
)
AS
BEGIN
INSERT INTO PHIEUTHU(MASV, NIENKHOA, HOCKY) VALUES (@MASV, @NIENKHOA, @HOCKY)
END
GO

CREATE PROCEDURE sp_PHIEUTHU_update(
@MAPT int,
@MASV nvarchar(15),
@NIENKHOA nvarchar(9),
@HOCKY int
)
AS
BEGIN
UPDATE PHIEUTHU SET MASV = @MASV, NIENKHOA = @NIENKHOA, HOCKY = @HOCKY WHERE MAPT = @MAPT
END
GO

CREATE PROCEDURE sp_PHIEUTHU_delete(
@MAPT int
)
AS
BEGIN
DELETE FROM PHIEUTHU WHERE MAPT = @MAPT
END
GO

CREATE PROCEDURE sp_PHIEUTHU_select_all
AS
BEGIN
SELECT * FROM PHIEUTHU
END
GO

CREATE PROCEDURE sp_PHIEUTHU_select_mapt(
	@MAPT int
)
AS
BEGIN
SELECT * FROM PHIEUTHU WHERE MAPT = @MAPT
END
GO


CREATE PROCEDURE sp_PHIEUTHU_select_masv(
	@MASV nvarchar(15)
)
AS
BEGIN
SELECT * FROM PHIEUTHU WHERE MASV = @MASV
END
GO

CREATE PROCEDURE sp_PHIEUTHU_select_masv_hocky(
	@MASV nvarchar(15),
	@HOCKY int
)
AS
BEGIN
SELECT * FROM PHIEUTHU WHERE MASV = @MASV and HOCKY = @HOCKY
END
GO

